    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String [] dato = new String [6];   
//validar
        if(txnombre.getText().equals("") && txapeP.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campos de tecto estan vacios", "Advertencia",JOptionPane.WARNING_MESSAGE);
        }else{
             /**
        * mandar los datos del usuario al directorio
        **/
            dato[0]=txnombre.getText();
            dato[1]=txapeP.getText();
            dato[2]=txapeM.getText();
            dato[3]=fecha.getText();
            dato[4]=txedad.getText();
            dato[5]=codigo.getText();
            dir2.setFilas(dato);//enviar datos a la tabla 
        }//fin de if 
    }//GEN-LAST:event_jButton3ActionPerformed

